# 1. Sample tuple
tuple1 = ("Mars" , "Saturn" , "Jupiter")
print(tuple1)

# 2. Allow Duplicates
tuple2 = ("Neptune","Earth","Pluto","Pluto")
print(tuple2)

# 3. Tuple Length
tuple3 = ("Neptune","Earth","Pluto","Pluto")
print(len(tuple3))

# 4. Create Tuple with one item
tuple4 = ("Neptune",)
print(type(tuple4))

# 5. Data types in tuple
tuple5 = ("Neptune","Earth","Pluto","Pluto")
tuple6 = (1 , 2 , 3)
tuple7 = (True , False , True)
print(type(tuple5))
print(type(tuple6))
print(type(tuple7))

# 6. tuple constructor
thistupple = tuple(("apple" , "Banana" , "Cherry"))
print(thistupple)
