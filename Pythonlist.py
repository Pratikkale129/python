# 1. Pyhton List
thislist = ["Apple" , "banana" , "Cherry"]
print(thislist)

# 2. Access List items
thislist1 = ["Orange" , "Pink" , "Red"]
print(thislist1)
print(thislist1[1])

# 3. Change List Items
thislist2 = ["Apple" , "Banana" , "Cherry"]
thislist2[1] = "Blackberry"
print(thislist2)

# 4. Add List Items
thislist3 = ["BMW" , "Mercedes" , "Audi"]
thislist3.append("Dodge")
print(thislist3)

# 5. Remove List Items
myCar = ["BMW" , "Dodge" , "Audi"]
myCar.remove("Audi")
print(myCar)
